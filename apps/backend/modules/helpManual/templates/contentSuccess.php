</span></span><span >签到记录</span></a></h2>

<p class=MsoNormal><span >签到记录允许有对应权限的人查看外勤人员的签到情况。可以根据项目筛选，或者根据时间段进行过滤。</span></p>

<p class=MsoNormal><span lang=EN-US><img border=0 width=554 height=178
id="图片 90" src="/images/helpManual/image082.jpg"></span></p>


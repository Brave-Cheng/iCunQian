<h2 style='margin-left:1.0cm;text-indent:-1.0cm'><a name="_Toc376966107"><span
lang=EN-US>3.5<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><span >审批流程</span></a></h2>

<p class=MsoNormal><span >审批流程允许对项目上产生的表格进行审批。</span></p>

<p class=MsoNormal><span >用户可以在这里查看所有和个人相关的审批：包括需要自己审批的，以及自己所提交的审批。</span></p>

<p class=MsoNormal><span lang=EN-US><img border=0 width=554 height=253
id="图片 95" src="/images/helpManual/image079.jpg"></span></p>

<p class=MsoNormal><span >审批完的表格，将只允许查看。</span></p>

<p class=MsoNormal><span >审批中的表格，则需要填入审批意见并选择同意或者驳回。</span></p>

<p class=MsoNormal><span lang=EN-US><img border=0 width=554 height=340
id="图片 96" src="/images/helpManual/image080.jpg"></span></p>


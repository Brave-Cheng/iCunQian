<h1 style='margin-left:21.25pt;text-indent:-21.25pt'><a name="_Toc376966102"><span
lang=EN-US>3<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><span >各模块功能简介</span></a></h1>

<h2 style='margin-left:1.0cm;text-indent:-1.0cm'><a name="_Toc376966103"><span
lang=EN-US>3.1<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><span >个人中心</span></a></h2>

<p class=MsoNormal><span >个人中心为用户提供了快捷入口，允许用户为所在项目撰写日报，并且修改个人资料。</span></p>

<p class=MsoNormal><span lang=EN-US><img border=0 width=543 height=114
id="图片 82" src="/images/helpManual/image069.png"></span></p>

<p class=MsoNormal><span >个人中心的下半部分列出了登录用户所收到的通知，并允许用户对通知做出新建、删除等管理。</span></p>

<p class=MsoNormal><span lang=EN-US><img border=0 width=553 height=271
id="图片 83" src="/images/helpManual/image070.jpg"></span></p>

<p class=MsoNormal><span >其中，带有</span><span lang=EN-US><img
border=0 width=21 height=22 id="图片 84"
src="/images/helpManual/image071.png"></span><span style='font-family:
宋体'>的通知为已读。</span><span lang=EN-US><img border=0 width=21 height=17 id="图片 85"
src="/images/helpManual/image072.png"></span><span style='font-family:
宋体'>的通知为未读。蓝色加粗的文字表示今天收到的未读通知。发件人为“系统邮件”的通知主要都是和审批以及项目进度有关的。</span></p>

<p class=MsoNormal><span >点击通知标题将可以查看具体的通知内容：</span></p>

<p class=MsoNormal><span lang=EN-US><img border=0 width=554 height=108
id="图片 86" src="/images/helpManual/image073.jpg"></span></p>

<h2 style='margin-left:1.0cm;text-indent:-1.0cm'><a name="_Toc376966104"><span
lang=EN-US>3.2<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><span >组织结构</span></a></h2>

<p class=MsoNormal><span >组织结构允许登录用户查看系统内各个部门同事的信息：姓名、手机号码以及他们当前所位于的项目。</span></p>

<p class=MsoNormal><span lang=EN-US><img border=0 width=554 height=198
id="图片 87" src="/images/helpManual/image074.jpg"></span></p>

<p class=MsoNormal><span >点击部门名称可以切换到对应的部门。</span></p>

<p class=MsoNormal><span >拥有权限的超级管理员可以新建用户并对用户进行编辑、删除等管理操作。</span></p>

<h2 style='margin-left:1.0cm;text-indent:-1.0cm'><a name="_Toc376966105"><span
lang=EN-US>3.3<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><span >文档管理</span></a></h2>

<p class=MsoNormal><span >文档管理允许用户对于文档记录进行管理：新建、删除、编辑。</span></p>

<p class=MsoNormal><span >文档必须关联项目。</span></p>

<p class=MsoNormal><span >可以选择项目对文档列表进行过滤。</span></p>

<p class=MsoNormal><span lang=EN-US><img border=0 width=553 height=323
id="图片 88" src="/images/helpManual/image075.jpg"></span></p>

<h2 style='margin-left:1.0cm;text-indent:-1.0cm'><a name="_Toc376966106"><span
lang=EN-US>3.4<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><span >项目管理</span></a></h2>

<p class=MsoNormal><span >项目管理允许用户对项目进行操作。</span></p>

<p class=MsoNormal><span >列表中列出了所有用户有权限查看的项目：自己创建的项目或者是自己属于的项目。</span></p>

<p class=MsoNormal><span lang=EN-US><img border=0 width=554 height=295
id="图片 92" src="/images/helpManual/image076.jpg"></span></p>

<p class=MsoNormal><span >点击项目汇总可以查看项目的整体情况。</span></p>

<p class=MsoNormal><span >点击修改属性，将可以修改项目的属性。</span></p>

<p class=MsoNormal><span >点击修改成员，将可以对项目成员进行添加删除或者是为他们重新分配角色。</span></p>

<p class=MsoNormal><span >点击管理文档，可以将文档记录和项目关联。</span></p>

<p class=MsoNormal><span >点击跟踪进度，将可以查看现在所分期的项目进度：</span></p>

<p class=MsoNormal><span lang=EN-US><img border=0 width=553 height=157
id="图片 93" src="/images/helpManual/image077.jpg"></span></p>

<p class=MsoNormal><span >在项目阶段处点击“完成”，将会给项目经理发送通知，告知项目经理，该阶段已经完成。项目经理确认后，再次点击完成，则将该项目阶段正式标记为完成。</span></p>

<p class=MsoNormal><span lang=EN-US><img border=0 width=553 height=121
id="图片 94" src="/images/helpManual/image078.jpg"></span></p>

<p class=MsoNormal><span >完成后的项目阶段将无法进行修改或者删除。</span></p>

<p class=MsoNormal><span >点击终止可以让项目终止。</span></p>

<p class=MsoNormal><span >点击查看项目日报则进入项目日报列表。如果是项目分管副总，可以查看所有项目成员的日报，如果是其他项目成员，则只可以查看自己的日报。</span></p>

<p class=MsoNormal><span >注意：对于投标项目还有以下两个操作选项：</span></p>

<p class=MsoNormal><span >点击中标，可以将项目标记为“正式展开”的公司项目，从而允许进行项目进度的跟踪。</span></p>

<p class=MsoNormal><span >点击查看历史，则可以查看哪些人，对该项目进行了什么样的属性修改。</span></p>

<span lang=EN-US style='font-size:11.0pt;line-height:115%;font-family:"Calibri","sans-serif"'><br
clear=all style='page-break-before:always'>

<h2 style='margin-left:1.0cm;text-indent:-1.0cm'><a name="_Toc376966108"><span
lang=EN-US>3.6<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><span >视频监控</span></a></h2>

<p class=MsoNormal><span >视频监控允许使用对应的视频摄像器材对分公司地点进行监控。</span></p>

